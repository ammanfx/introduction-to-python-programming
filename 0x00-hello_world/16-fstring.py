#!/bin/env python3 
"""A module used for storing product name"""


# Store product name and price
product = "Laptop"
price = 750.5

# Print using f-string with two decimal places
print(f"The price of {product} is ${price:.2f}")

# Store student's name and score
student_name = "Alice"
score = 92

# Print using .format() method
print("Student {} scored {} points".format(student_name, score))