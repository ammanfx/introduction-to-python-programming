#!/bin/env python3
"""A module that perform division operation"""



def division(num1, num2):
    """
    A function that divide two numbers
    params:
        num1 - takes any number
        num2 - takes any number
    return the result of both operands

    """
    return num1 / num2


if __name__ == '__main__':
    result = division(20, 2)
    print(result)
