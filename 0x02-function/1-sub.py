#!/bin/env python3
"""A module that perform subtraction operation"""



def sub(num1, num2):
    """
    A function that sub two numbers
    params:
        num1 - takes any number
        num2 - takes any number
    return the result of both operands

    """
    return num1 - num2


if __name__ == '__main__':
    result = sub(10, 5)
    print(result)