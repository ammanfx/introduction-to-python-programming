#!/usr/bin/python3
"""A module that performs addition operation"""


def add(num1, num2):
    """
    A function that add two numbers
    params:
        num1 - takes any number
        num2 - takes any number
    return the result of both operands

    """
    return num1 + num2



if __name__ == '__main__':
    result = add(5, 8)
